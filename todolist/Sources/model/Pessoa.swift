class Pessoa {
    var nome: String
    var idade: Int
    var time:String
    var posicao:String
    
    
    init(nome: String, idade: Int, time:String, posicao:String) {
        self.nome = nome
        self.idade = idade
        self.time = time
        self.posicao = posicao
    }
    
   
}

